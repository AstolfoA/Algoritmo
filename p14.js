// . Invertir un array

// Enunciado:
// Dado un array, retorna un nuevo array con los elementos en orden inverso.

// Entrada:

// [1, 2, 3, 4, 5]


// Salida:

// [5, 4, 3, 2, 1]

const numbers = [1, 2, 3, 4, 5];
const investValue = (array) => {
    return array.reverse()
}
console.log(investValue(numbers))